function getFallbackReply(message) {
    const lowerMsg = message.toLowerCase();
    for (const [keyword, reply] of Object.entries(fallbackReplies)) {
        if (lowerMsg.includes(keyword)) {
            return reply;
        }
    }
    return null;
}

// ========== SiliconFlow API ==========
function callAI(apiKey, messages) {
    return new Promise((resolve, reject) => {
        const postData = JSON.stringify({
            model: 'Qwen/Qwen2.5-7B-Instruct',
            messages: messages,
            temperature: 0.7,
            max_tokens: 2000
        });

        const options = {
            hostname: 'api.siliconflow.cn',
            port: 443,
            path: '/v1/chat/completions',
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
                'Authorization': `Bearer ${apiKey}`,
                'Content-Length': Buffer.byteLength(postData)
            }
        };

        const req = https.request(options, (response) => {
            let data = '';
            response.on('data', chunk => data += chunk);
            response.on('end', () => {
                try {
                    const result = JSON.parse(data);
                    if (response.statusCode !== 200) {
                        console.error('SiliconFlow API错误:', response.statusCode, data.substring(0, 200));
                        return reject(new Error(result.error?.message || 'AI服务请求失败'));
                    }
                    if (result.choices && result.choices[0]?.message?.content) {
                        resolve(result.choices[0].message.content);
                    } else {
                        reject(new Error('AI响应格式异常'));
                    }
                } catch (err) {
                    console.error('SiliconFlow响应解析失败:', data.substring(0, 200));
                    reject(new Error('AI服务响应解析失败'));
                }
            });
        });

        req.on('error', (error) => {
            console.error('SiliconFlow网络请求失败:', error.message);
            reject(new Error('AI服务网络连接异常'));
        });

        req.write(postData);
        req.end();
    });
}

router.post('/chat', async (req, res) => {
    try {
        const { message } = req.body;
        if (!message) {
            return res.status(400).json({ error: '请输入消息内容' });
        }

        // 优先匹配本地知识库，命中关键词直接返回，不调AI避免数字错乱
        const fallback = getFallbackReply(message);
        if (fallback) {
            return res.json({ reply: fallback });
        }

        const apiKey = process.env.SILICONFLOW_API_KEY;

        if (!apiKey || apiKey === 'your-siliconflow-api-key-here') {
            return res.json({ reply: '您好！我是智能客服小橘~ 您可以直接问我关于发布、交易、收藏等问题，我会尽力帮您解答！' });
        }

        try {
            const reply = await callAI(apiKey, [
                { role: 'system', content: SYSTEM_PROMPT },
                { role: 'user', content: message }
            ]);
            return res.json({ reply });
        } catch (apiError) {
            console.error('AI调用失败:', apiError.message);
            return res.json({ reply: '抱歉，AI服务暂时繁忙~ 您可以换一种方式描述您的问题，我会尽力帮您解答。' });
        }

    } catch (error) {
        console.error('聊天接口异常:', error);
        res.status(500).json({ error: '服务器内部错误' });
    }
});

